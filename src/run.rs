use std::num::Wrapping as W;

struct Instruction {
    operator: u16,
    operand: u16
}

const OP_INC_DP: u16 = 1;
const OP_DEC_DP: u16 = 2;
const OP_INC_VAL: u16 = 3;
const OP_DEC_VAL: u16 = 4;
const OP_OUT: u16 = 5;
const OP_IN: u16 = 6;
const OP_JMP_FWD: u16 = 7;
const OP_JMP_BCK: u16 = 8;

const DATA_SIZE: usize = 65535;

fn compile_bf(input: &str) -> Result<Vec<Instruction>, String> {
    let mut program: Vec<Instruction> = vec![];
    let mut pc = 0u16;
    let mut jmp_pc;
    let mut jmp_stack: Vec<u16> = vec![];
    for c in input.chars() {
        match c {
            '>' => {
                program.push(Instruction{ operator: OP_INC_DP, operand: 0 });
            },
            '<' => {
                program.push(Instruction{ operator: OP_DEC_DP, operand: 0 });
            },
            '+' => {
                program.push(Instruction{ operator: OP_INC_VAL, operand: 0 });
            },
            '-' => {
                program.push(Instruction{ operator: OP_DEC_VAL, operand: 0 });
            },
            '.' => {
                program.push(Instruction{ operator: OP_OUT, operand: 0 });
            },
            ',' => {
                program.push(Instruction{ operator: OP_IN, operand: 0 });
            },
            '[' => {
                program.push(Instruction{ operator: OP_JMP_FWD, operand: 0 });
                jmp_stack.push(pc);
            },
            ']' => {
                if jmp_stack.len() == 0 {
                    return Err(String::from("Compilation error."));
                }
                jmp_pc = jmp_stack.last().unwrap().clone();
                jmp_stack = Vec::from(jmp_stack.split_last().unwrap().1);
                program.push(Instruction{ operator: OP_JMP_BCK, operand: jmp_pc });
                program.get_mut(jmp_pc as usize).unwrap().operand = pc;
            },
            _ => {
                pc -= 1;
            }
        }
        pc += 1;
    }
    if jmp_stack.len() != 0 {
        return Err(String::from("Compilation error."))
    }
    Ok(program)
}

fn execute_bf(program: &[Instruction]) {
    let mut data = [W(0i16); DATA_SIZE];
    let mut data_ptr: usize = 0;
    let mut pc = 0;
    while pc < program.len() {
        match program[pc].operator {
            OP_INC_DP => {
                data_ptr += 1;
            },
            OP_DEC_DP => {
                data_ptr -= 1;
            },
            OP_INC_VAL => {
                data[data_ptr] += W(1i16);
            },
            OP_DEC_VAL => {
                data[data_ptr] -= std::num::Wrapping(1i16);
            }
            OP_OUT => {
                print!("{}", std::char::from_u32(data[data_ptr].0 as u32).unwrap());
            },
            OP_IN => {
                data[data_ptr] = W(0i16);
            },
            OP_JMP_FWD => {
                if data[data_ptr] == W(0i16) {
                    pc = program[pc].operand as usize;
                }
            },
            OP_JMP_BCK => {
                if data[data_ptr] > W(0i16) {
                    pc = program[pc].operand as usize;
                }
            }
            _ => {
                panic!("Unknown operator.");
            }
        }
        pc += 1;
    }
}

